package tema5.resumen;

/**
 * Crea un array de 3 números enteros. Asigna a cada elemento un valor,
 * haciéndolo en línea aparte cada uno. Muestra en la salida estándar (pantalla)
 * esos valores. Por separado.
 */
public class Ejercicio1 {

	public static void main(String[] args) {
		int[] numerosEnteros = new int[3];
		
		numerosEnteros[0] = 87;
		numerosEnteros[1] = 10;
		numerosEnteros[2] = 29;
		
		System.out.println(numerosEnteros[0]);
		System.out.println(numerosEnteros[1]);
		System.out.println(numerosEnteros[2]);
	}

}
