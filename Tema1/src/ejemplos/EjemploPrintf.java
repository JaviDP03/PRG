public class EjemploPrintf {
    public static void main(String[] args) {
        System.out.printf("Hola mundo.\n");
        System.out.printf("\t¿Qué \"tal\" estás?\n");
        System.out.printf("Mi color favorito es: %s.\n", "azul");
        System.out.printf("Me llamo %s y me gusta el color %s.\n", "Javier", "azul");
        System.out.printf("Mi teléfono es %010d y me cobran %3.5f al mes.\n", 435763124, 129.99);
    }
}
